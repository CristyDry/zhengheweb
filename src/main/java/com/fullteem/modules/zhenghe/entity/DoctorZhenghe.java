package com.fullteem.modules.zhenghe.entity;

public class DoctorZhenghe {
	
	private String id;					//医生id
	private String avatar;				//头像
	private String doctor;				//姓名
	private String department;			//科室名称
	private String professional;		//职称
	private String hospital;			//医院名称
	private String professionalField;	//专业领域
	private String intro;				//个人简介
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public String getDoctor() {
		return doctor;
	}
	public void setDoctor(String doctor) {
		this.doctor = doctor;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getProfessional() {
		return professional;
	}
	public void setProfessional(String professional) {
		this.professional = professional;
	}
	public String getHospital() {
		return hospital;
	}
	public void setHospital(String hospital) {
		this.hospital = hospital;
	}
	public String getProfessionalField() {
		return professionalField;
	}
	public void setProfessionalField(String professionalField) {
		this.professionalField = professionalField;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	
	
	
}
